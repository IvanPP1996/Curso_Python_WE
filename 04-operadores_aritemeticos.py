#suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5
#multiplicacion y división (* y /)
multi = 12 * 5
division = 12 / 6 #Nos devuelve un dato flotante
#Potenciación (**)
exponete = 12**5
#División baja
division_baja = 12 // 5 #Nos devuelve el dato entero redondeado hacia abajo
#Moulo o resto (%)
resto = 12 % 5

print(suma)
print(resta)
print(multi)
print(division)
print(exponete)
print(division_baja)
print(resto)

